<h1> Tipos de Render </h1>
<p>SSR CSR SSG</p>

<b>Client Side Renderging (CSR)</b>
<p>Es el tipo de renderizado mas comun en librerias como React.js o Vue.js Aqui es el navegador
del cliente quien, por medio de codigo Javascript, se encarga de armar toda la estructura de la pagina web</p>

<b>Server Side Rendergin (SSR) </b>
<p> Es el tipo de renderizado mas comun en la web este renderizado se basa en que es nuestro servidor
    quien procesa todo el codigo y manda todo el codigo ya procesado a nuestro navegador.
    Es el mas comun en lenguajes backend
</p>
<b> Static Side Generation  (SSG) </b>
<p>
    Este tipo de renderizado se hace desde que construimos nuestra aplicacion en tiempo de desarrollo. 
    Es comun verlo cuando trabajamos con frameworks como Next.js o Nuxt.js
    <br>
    Todo el HTML es construido en tiempo de build. es decir, desde que desarrollamos, 
    lo que hace que no sea necesario un servidor
</p>