<h2> Operadores aritméticos: </h2>
Suma: Para sumar dos valores usamos el signo +. <br>
Resta: Para restar dos valores usamos el signo -. <br>
Multiplicación: Para multiplicar dos valores usamos el signo *. <br>
División: Para dividir dos valores usamos el signo /. <br>
Incremento: Sirve para incrementar un valor numérico en 1, usamos el operador ++. Ejemplo: $value++, sumará 1 a $value. <br>
Decremento: Sirve para disminuir un valor numérico en 1, usamos el operador --. Ejemplo: $value--, restará 1 a $value. <br>
